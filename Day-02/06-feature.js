let a = 7;
let b = 14;

console.log('a > b:', a > b);    // Output:- a > b: false
console.log('a < b:', a < b);    // Output:- a < b: true
console.log('a == b:', a == b);  // Output:- a == b: false
console.log('a != b:', a != b);  // Output:- a != b: true
console.log('a >= b:', a >= b);  // Output:- a >= b: false
console.log('a <= b:', a <= b);  // Output:- a <= b: true

console.log('a < b && a != b:', a < b && a != b);  // Output:- true 
console.log('a > b || a == b:', a > b || a == b);  // Output:- false 
console.log('!(a == b):', !(a == b));              // Output:- true 

